import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const DOCS_APP = path.resolve(__dirname, '..');
const REPO_ROOT = path.resolve(DOCS_APP, '../..');
const GENERATED_ROOT = path.join(DOCS_APP, 'app/docs/(generated)');

const SKIP_DIRS = new Set(['node_modules', 'dist', 'build', '.turbo', '.next', 'coverage', 'docs', 'src', 'api']);
const WATCH = process.argv.includes('--watch');

/**
 * Per-workspace exclude patterns (relative to the workspace's `docs/` dir, no
 * file extension). Use `<dir>/**` to exclude everything under a directory.
 *
 * Why: customer-facing error pages under apps/landing/docs/errors/ use Markdoc
 * syntax for the customer-facing site; Nextra's acorn-based MDX parser fails
 * on them. Keep them out of this site.
 */
const WORKSPACE_EXCLUDES = {
    landing: ['errors/**'],
};

function matchGlob(pattern, str) {
    // Simple `**` glob: only prefix matching is needed for now (`errors/**`
    // matches anything under `errors/`).
    if (pattern.endsWith('/**')) {
        const prefix = pattern.slice(0, -3);
        return str === prefix || str.startsWith(`${prefix}/`);
    }
    return pattern === str;
}

function isExcluded(slug, relPath) {
    const patterns = WORKSPACE_EXCLUDES[slug];
    if (!patterns) return false;
    return patterns.some((p) => matchGlob(p, relPath));
}

function discoverWorkspaces() {
    const out = [];
    for (const parent of ['apps', 'packages']) {
        const root = path.join(REPO_ROOT, parent);
        if (!fs.existsSync(root)) continue;
        for (const entry of fs.readdirSync(root, { withFileTypes: true })) {
            if (!entry.isDirectory()) continue;
            if (SKIP_DIRS.has(entry.name) || entry.name.startsWith('.')) continue;
            walk(path.join(root, entry.name), [entry.name], out);
        }
    }
    return out;
}

function walk(dir, segments, out) {
    if (fs.existsSync(path.join(dir, 'docs')) && fs.existsSync(path.join(dir, 'package.json'))) {
        out.push({ slug: segments.join('/'), rootPath: dir, docsPath: path.join(dir, 'docs') });
        return;
    }
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
        if (!entry.isDirectory()) continue;
        if (SKIP_DIRS.has(entry.name) || entry.name.startsWith('.')) continue;
        walk(path.join(dir, entry.name), [...segments, entry.name], out);
    }
}

function sweep() {
    if (fs.existsSync(GENERATED_ROOT)) {
        fs.rmSync(GENERATED_ROOT, { recursive: true, force: true });
    }
    fs.mkdirSync(GENERATED_ROOT, { recursive: true });
}

function mirrorFile(src, dest) {
    fs.mkdirSync(path.dirname(dest), { recursive: true });
    try {
        fs.linkSync(src, dest);
    } catch (err) {
        // Cross-device / permission error: fall back to copy with warning.
        console.warn(`[mirror] hardlink failed for ${src} -> ${dest}; copying instead (${err.code})`);
        fs.copyFileSync(src, dest);
    }
}

function* walkDocs(dir) {
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
        const full = path.join(dir, entry.name);
        if (entry.isDirectory()) yield* walkDocs(full);
        else if (entry.isFile() && (entry.name.endsWith('.mdx') || entry.name.endsWith('.md'))) yield full;
    }
}

function mirrorWorkspace(ws) {
    let linked = 0;
    for (const src of walkDocs(ws.docsPath)) {
        const relFromDocs = path.relative(ws.docsPath, src);
        const withoutExt = relFromDocs.replace(/\.(mdx|md)$/, '');
        if (isExcluded(ws.slug, withoutExt)) continue;
        const dest = path.join(GENERATED_ROOT, ws.slug, withoutExt, 'page.mdx');
        mirrorFile(src, dest);
        linked++;
    }

    // Drop a README in each workspace's generated dir.
    const wsDir = path.join(GENERATED_ROOT, ws.slug);
    if (fs.existsSync(wsDir)) {
        fs.writeFileSync(
            path.join(wsDir, 'README.md'),
            `Generated by apps/docs/scripts/mirror-workspace-docs.mjs.\nSource: ${path.relative(REPO_ROOT, ws.docsPath)}/\nDo not edit; changes will be lost on next build.\n`,
        );
    }

    return linked;
}

function main() {
    sweep();
    const workspaces = discoverWorkspaces();
    let total = 0;
    for (const ws of workspaces) {
        total += mirrorWorkspace(ws);
    }
    console.info(`[mirror-workspace-docs] mirrored ${total} pages across ${workspaces.length} workspace(s)`);
}

if (WATCH) {
    let timer;
    const watch = (dir) => {
        try {
            fs.watch(dir, { recursive: true }, () => {
                clearTimeout(timer);
                timer = setTimeout(() => main(), 300);
            });
        } catch {}
    };
    for (const parent of ['apps', 'packages']) {
        watch(path.join(REPO_ROOT, parent));
    }
    main();
} else {
    main();
}
